"use client"

import { useState, useCallback } from "react"
import { toast } from "sonner"
import { createClient } from "@/lib/supabase/client"
import { writeAuditLogClient } from "@/lib/audit/client"
import { logger } from "@/lib/logger"

const log = logger("reports-mail-weekly-summary-send")

type ContentChoice =
  | "weekly_report"
  | "action_points_autogenerated"
  | "knowledge_sharing_session"
  | "minutes_of_meeting"

type SendTiming = "now" | "scheduled" | "recurring"

interface DocRef {
  id: string
  file_name: string
  display_name?: string
  signed_url?: string | null
}

async function fetchStoredMeetingDocumentAttachment(doc: DocRef): Promise<{ base64: string; filename: string }> {
  if (!doc.signed_url) throw new Error(`Missing download URL for ${doc.display_name || doc.file_name || doc.id}`)

  const response = await fetch(doc.signed_url)
  if (!response.ok) {
    throw new Error(`Failed to download ${doc.display_name || doc.file_name || doc.id}`)
  }

  return {
    base64: await toBase64FromBlob(await response.blob()),
    filename: doc.display_name || doc.file_name || "meeting-document.pdf",
  }
}

interface UseSendParams {
  resolvedRecipients: string[]
  selectedContentChoices: Set<ContentChoice>
  allKssDocs: DocRef[]
  allMinutesDocs: DocRef[]
  sendTiming: SendTiming
  scheduledDate: string
  scheduledTime: string
  recurringDay: string
  recurringTime: string
  selectedWeeks: Set<number>
  yearNumber: number
  selectedPreparedBy: string
  selectedPreparedByDesignation: string | null
  selectedPreparedByDepartment: string | null
  currentUser?: { id?: string; department?: string | null; full_name?: string | null }
  fetchSchedules: () => void
}

function capitalize(s: string): string {
  return s.charAt(0).toUpperCase() + s.slice(1)
}

function readFilenameFromDisposition(disposition: string | null, fallback: string) {
  if (!disposition) return fallback
  const utf8Match = disposition.match(/filename\*=UTF-8''([^;]+)/i)
  if (utf8Match?.[1]) {
    try {
      return decodeURIComponent(utf8Match[1])
    } catch {
      return utf8Match[1]
    }
  }

  const plainMatch = disposition.match(/filename="([^"]+)"/i) || disposition.match(/filename=([^;]+)/i)
  return plainMatch?.[1]?.trim() || fallback
}

async function toBase64FromBlob(blob: Blob): Promise<string> {
  return await new Promise<string>((resolve, reject) => {
    const reader = new FileReader()

    reader.onerror = () => reject(reader.error ?? new Error("Failed to encode attachment"))
    reader.onloadend = () => {
      const result = typeof reader.result === "string" ? reader.result : ""
      const base64 = result.includes(",") ? result.split(",", 2)[1] : ""

      if (!base64) {
        reject(new Error("Failed to encode attachment"))
        return
      }

      resolve(base64)
    }

    reader.readAsDataURL(blob)
  })
}

async function fetchActionPointsAttachment(params: {
  week: number
  year: number
}): Promise<{ blob: Blob; filename: string }> {
  const response = await fetch("/api/reports/weekly-report-export", {
    method: "POST",
    headers: {
      "Content-Type": "application/json",
    },
    body: JSON.stringify({
      week: params.week,
      year: params.year,
      type: "action_point",
      persist: true,
      reuseStored: true,
    }),
  })

  if (!response.ok) {
    const detail = await response.text().catch(() => "")
    throw new Error(detail || "Failed to generate Action Points PDF")
  }

  const exportBlob = await response.blob()
  return {
    blob: exportBlob,
    filename: readFilenameFromDisposition(
      response.headers.get("Content-Disposition"),
      `ACOB Action Points - Week ${params.week}.pdf`
    ),
  }
}

/**
 * Downloads a stored meeting document (KSS, Minutes, etc.) via its signed URL
 * and converts it to base64 — client-side, so the edge function never has to
 * do the CPU-heavy byte-by-byte conversion itself.
 */
async function fetchWeeklyReportAttachment(params: {
  week: number
  year: number
}): Promise<{ blob: Blob; filename: string }> {
  const response = await fetch("/api/reports/weekly-report-export", {
    method: "POST",
    headers: { "Content-Type": "application/json" },
    body: JSON.stringify({
      week: params.week,
      year: params.year,
      type: "weekly_report",
      persist: true,
      reuseStored: true,
    }),
  })

  if (!response.ok) {
    const detail = await response.text().catch(() => "")
    throw new Error(detail || "Failed to generate Weekly Report PDF")
  }

  const exportBlob = await response.blob()
  return {
    blob: exportBlob,
    filename: readFilenameFromDisposition(
      response.headers.get("Content-Disposition"),
      `ACOB Weekly Reports - Week ${params.week}.pdf`
    ),
  }
}

export function useWeeklySummarySend({
  resolvedRecipients,
  selectedContentChoices,
  allKssDocs,
  allMinutesDocs,
  sendTiming,
  scheduledDate,
  scheduledTime,
  recurringDay,
  recurringTime,
  selectedWeeks,
  yearNumber,
  selectedPreparedBy,
  selectedPreparedByDesignation,
  selectedPreparedByDepartment,
  currentUser,
  fetchSchedules,
}: UseSendParams) {
  // Sorted array of selected weeks for iteration
  const weekNumbers = Array.from(selectedWeeks).sort((a, b) => a - b)
  const supabase = createClient()
  const [isSending, setIsSending] = useState(false)
  // eslint-disable-next-line @typescript-eslint/no-explicit-any
  const [sendResult, setSendResult] = useState<any>(null)

  const logMailAudit = useCallback(
    async (params: { action: string; entityId: string; metadata?: Record<string, unknown> }) => {
      try {
        await writeAuditLogClient(
          // eslint-disable-next-line @typescript-eslint/no-explicit-any
          supabase as any,
          {
            action: "send",
            entityType: "mail_summary",
            entityId: params.entityId,
            metadata: { ...(params.metadata || {}), event: params.action },
            context: {
              actorId: currentUser?.id || undefined,
              department: currentUser?.department || "Admin & HR",
              source: "ui",
              route: "/admin/reports/weekly-summary",
            },
          },
          { failOpen: true }
        )
      } catch (error) {
        log.error("[weekly-report] Failed to write audit log", error)
      }
    },
    [currentUser?.department, currentUser?.id, supabase]
  )

  const doSend = useCallback(async () => {
    setIsSending(true)
    setSendResult(null)

    const weekLabel = weekNumbers.length === 1 ? `Week ${weekNumbers[0]}` : `${weekNumbers.length} weeks`

    const toastId = toast.loading(`Sending ${weekLabel} to ${resolvedRecipients.length} recipient(s)...`)

    try {
      const supabaseUrl = process.env.NEXT_PUBLIC_SUPABASE_URL
      const supabaseKey = process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY

      // ── Pre-generate Weekly Report PDF client-side ──────────────────────────
      // This moves the CPU-heavy pdf-lib work out of the edge function so it
      // never hits Supabase's 2-second CPU limit mid-send.
      let weeklyReportBase64: string | undefined
      let weeklyReportFilename: string | undefined

      if (selectedContentChoices.has("weekly_report")) {
        const reportFile = await fetchWeeklyReportAttachment({
          week: weekNumbers[0],
          year: yearNumber,
        })
        weeklyReportBase64 = await toBase64FromBlob(reportFile.blob)
        weeklyReportFilename = reportFile.filename
      }

      // ── KSS / Minutes: pass document IDs to the edge function ─────────────
      // The edge function now uses a chunk-based base64 conversion that is
      // ~100× faster than the old byte-by-byte loop, so server-side download
      // is fast and avoids uploading large file payloads over the user's
      // internet connection.

      // ── Build per-week action points attachments ────────────────────────────
      const actionPointAttachments: Array<{ base64: string; filename: string; week: number }> = []
      const additionalDocumentAttachments: Array<{ base64: string; filename: string }> = []
      const fallbackAdditionalDocumentIds: string[] = []

      if (selectedContentChoices.has("action_points_autogenerated")) {
        const generatedAttachments = await Promise.all(
          weekNumbers.map(async (weekNum) => {
            const exportFile = await fetchActionPointsAttachment({
              week: weekNum,
              year: yearNumber,
            })

            return {
              base64: await toBase64FromBlob(exportFile.blob),
              filename: exportFile.filename,
              week: weekNum,
            }
          })
        )

        actionPointAttachments.push(...generatedAttachments)
      }

      const selectedAdditionalDocs = [
        ...(selectedContentChoices.has("knowledge_sharing_session") ? allKssDocs : []),
        ...(selectedContentChoices.has("minutes_of_meeting") ? allMinutesDocs : []),
      ]

      if (selectedAdditionalDocs.length > 0) {
        const prefetchedDocs = await Promise.all(
          selectedAdditionalDocs.map(async (doc) => {
            try {
              return await fetchStoredMeetingDocumentAttachment(doc)
            } catch {
              if (doc.id) fallbackAdditionalDocumentIds.push(doc.id)
              return null
            }
          })
        )

        prefetchedDocs.forEach((doc) => {
          if (doc) additionalDocumentAttachments.push(doc)
        })
      }

      // ── Build payload ───────────────────────────────────────────────────────
      // eslint-disable-next-line @typescript-eslint/no-explicit-any
      const payload: any = {
        recipients: resolvedRecipients,
        // Pass all selected weeks; backend will include one report per week
        meetingWeeks: weekNumbers,
        meetingYear: yearNumber,
        // Legacy single-week field kept for backward-compat if only one week
        meetingWeek: weekNumbers[0],
        preparedByName: selectedPreparedBy,
        preparedByDesignation: selectedPreparedByDesignation,
        preparedByDepartment: selectedPreparedByDepartment || "Admin & HR",
        requestedByUserId: currentUser?.id || null,
        skipWeeklyReport: !selectedContentChoices.has("weekly_report"),
        skipActionPoint: !selectedContentChoices.has("action_points_autogenerated"),
      }

      // Collect document IDs for KSS and Minutes — edge function downloads
      // and converts them server-side using the fast chunk-based method.
      if (additionalDocumentAttachments.length > 0) {
        payload.additionalDocumentAttachments = additionalDocumentAttachments
      }
      if (fallbackAdditionalDocumentIds.length > 0) {
        payload.additionalDocumentIds = fallbackAdditionalDocumentIds
      }

      // Pass the pre-generated weekly report PDF so the edge function skips
      // its own (CPU-expensive) PDF generation entirely.
      if (weeklyReportBase64) {
        payload.weeklyReportBase64 = weeklyReportBase64
        payload.weeklyReportFilename = weeklyReportFilename
      }

      if (actionPointAttachments.length > 0) {
        // Multi-week: send array; single-week: keep legacy fields too
        payload.actionPointAttachments = actionPointAttachments
        payload.actionPointBase64 = actionPointAttachments[0].base64
        payload.actionPointFilename = actionPointAttachments[0].filename
      }

      const res = await fetch(`${supabaseUrl}/functions/v1/send-weekly-report`, {
        method: "POST",
        headers: {
          "Content-Type": "application/json",
          Authorization: `Bearer ${supabaseKey}`,
        },
        body: JSON.stringify(payload),
      })

      const result = await res.json()
      if (!res.ok) throw new Error(result.error || "Failed to send weekly report")

      setSendResult(result)
      // eslint-disable-next-line @typescript-eslint/no-explicit-any
      const successCount = result.results?.filter((r: any) => r.success).length || 0
      // eslint-disable-next-line @typescript-eslint/no-explicit-any
      const failCount = result.results?.filter((r: any) => !r.success).length || 0

      if (failCount === 0) {
        toast.success(`✅ ${weekLabel} sent to ${successCount} recipient(s)`, { id: toastId })
      } else {
        toast.warning(`Sent to ${successCount}, failed for ${failCount}`, { id: toastId })
      }

      await logMailAudit({
        action: "weekly_report_sent",
        entityId: crypto.randomUUID(),
        metadata: {
          meeting_weeks: weekNumbers,
          meeting_year: yearNumber,
          content_choices: Array.from(selectedContentChoices),
          prepared_by: selectedPreparedBy,
          success_count: successCount,
          failure_count: failCount,
          recipient_count: resolvedRecipients.length,
        },
      })
    } catch (err: unknown) {
      log.error("[Weekly Report Error]", err)
      toast.error(err instanceof Error ? err.message : "Failed to send weekly report", { id: toastId })
    } finally {
      setIsSending(false)
    }
  }, [
    resolvedRecipients,
    weekNumbers,
    yearNumber,
    selectedPreparedBy,
    selectedPreparedByDepartment,
    selectedPreparedByDesignation,
    selectedContentChoices,
    allKssDocs,
    allMinutesDocs,
    currentUser?.id,
    logMailAudit,
  ])

  const handleSend = useCallback(async () => {
    if (weekNumbers.length === 0) {
      toast.error("No weeks selected")
      return
    }
    if (resolvedRecipients.length === 0) {
      toast.error("No recipients selected")
      return
    }
    if (selectedContentChoices.size === 0) {
      toast.error("Select at least one content item")
      return
    }
    if (selectedContentChoices.has("knowledge_sharing_session") && allKssDocs.length === 0) {
      toast.error("No Knowledge Sharing Session files uploaded for the selected weeks")
      return
    }
    if (selectedContentChoices.has("minutes_of_meeting") && allMinutesDocs.length === 0) {
      toast.error("No Minutes of Meeting files uploaded for the selected weeks")
      return
    }
    if (
      sendTiming !== "now" &&
      (selectedContentChoices.has("knowledge_sharing_session") || selectedContentChoices.has("minutes_of_meeting"))
    ) {
      toast.error("Stored file send options can only be sent immediately")
      return
    }

    // ── Scheduled ──────────────────────────────────────────────────────────
    if (sendTiming === "scheduled") {
      if (!scheduledDate) {
        toast.error("Please set a scheduled date")
        return
      }
      const scheduledDateTime = new Date(`${scheduledDate}T${scheduledTime}`)
      if (scheduledDateTime <= new Date()) {
        toast.error("Scheduled time must be in the future")
        return
      }

      const includeWeeklyReport = selectedContentChoices.has("weekly_report")
      const includeActionPoint = selectedContentChoices.has("action_points_autogenerated")

      if (includeActionPoint) {
        toast.error("Exact Action Points PDF mail send is currently available with Send Now only")
        return
      }

      let scheduleContentChoice: "weekly_report" | "action_point" | "both"
      if (includeWeeklyReport && includeActionPoint) {
        scheduleContentChoice = "both"
      } else if (includeWeeklyReport) {
        scheduleContentChoice = "weekly_report"
      } else if (includeActionPoint) {
        scheduleContentChoice = "action_point"
      } else {
        toast.error("Only Weekly Report and Action Points can be scheduled")
        return
      }

      const { error } = await supabase.from("weekly_report_schedules").insert({
        schedule_type: "one_time",
        meeting_week: weekNumbers[0],
        meeting_weeks: weekNumbers,
        meeting_year: yearNumber,
        recipients: resolvedRecipients,
        content_choice: scheduleContentChoice,
        next_run_at: scheduledDateTime.toISOString(),
      })

      if (error) {
        toast.error("Failed to save schedule: " + error.message)
        return
      }

      toast.success(
        `Email scheduled for ${scheduledDateTime.toLocaleString("en-GB", {
          weekday: "short",
          day: "numeric",
          month: "short",
          hour: "2-digit",
          minute: "2-digit",
        })}`
      )
      fetchSchedules()
      return
    }

    // ── Recurring ──────────────────────────────────────────────────────────
    if (sendTiming === "recurring") {
      const days = ["sunday", "monday", "tuesday", "wednesday", "thursday", "friday", "saturday"]
      const targetDay = days.indexOf(recurringDay)
      const now = new Date()
      const currentDay = now.getDay()
      let daysUntil = targetDay - currentDay
      const [h, m] = recurringTime.split(":")
      const targetMinutes = parseInt(h, 10) * 60 + parseInt(m, 10)
      const nowMinutes = now.getHours() * 60 + now.getMinutes()
      if (daysUntil < 0 || (daysUntil === 0 && nowMinutes >= targetMinutes)) daysUntil += 7
      const nextRun = new Date(now)
      nextRun.setDate(now.getDate() + daysUntil)
      nextRun.setHours(parseInt(h, 10), parseInt(m, 10), 0, 0)

      const includeWeeklyReport = selectedContentChoices.has("weekly_report")
      const includeActionPoint = selectedContentChoices.has("action_points_autogenerated")
      if (includeActionPoint) {
        toast.error("Exact Action Points PDF mail send is currently available with Send Now only")
        return
      }
      if (!includeWeeklyReport && !includeActionPoint) {
        toast.error("Recurring mode supports Weekly Report and Action Points only")
        return
      }

      const recurringContentChoice: "weekly_report" | "action_point" | "both" =
        includeWeeklyReport && includeActionPoint ? "both" : includeWeeklyReport ? "weekly_report" : "action_point"

      const { error } = await supabase.from("weekly_report_schedules").insert({
        schedule_type: "recurring",
        recipients: resolvedRecipients,
        content_choice: recurringContentChoice,
        send_day: recurringDay,
        send_time: recurringTime,
        next_run_at: nextRun.toISOString(),
      })

      if (error) {
        toast.error("Failed to save recurring schedule: " + error.message)
        return
      }

      toast.success(`Recurring weekly email set for every ${capitalize(recurringDay)} at ${recurringTime}`)
      fetchSchedules()
      return
    }

    // ── Immediate ──────────────────────────────────────────────────────────
    await doSend()
  }, [
    resolvedRecipients,
    selectedContentChoices,
    allKssDocs,
    allMinutesDocs,
    sendTiming,
    scheduledDate,
    scheduledTime,
    recurringDay,
    recurringTime,
    weekNumbers,
    yearNumber,
    supabase,
    fetchSchedules,
    doSend,
  ])

  const deactivateSchedule = useCallback(
    async (id: string) => {
      const { error } = await supabase.from("weekly_report_schedules").update({ is_active: false }).eq("id", id)
      if (error) {
        toast.error("Failed to deactivate schedule")
      } else {
        toast.success("Schedule deactivated")
        fetchSchedules()
      }
    },
    [supabase, fetchSchedules]
  )

  return { isSending, sendResult, handleSend, deactivateSchedule }
}
